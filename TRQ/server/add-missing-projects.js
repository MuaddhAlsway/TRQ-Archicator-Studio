async function addMissingProjects() {
  try {
    const stmt = db.prepare(`
      INSERT INTO projects (
        title, category, subcategory,
        description, image, year, client,
        location, size, duration,
        challenge, solution,
        features, materials,
        awards, team, gallery,
        clientQuote, clientName, status,
        title_ar, category_ar, subcategory_ar,
        description_ar, client_ar,
        location_ar, size_ar, duration_ar,
        challenge_ar, solution_ar,
        features_ar, materials_ar,
        awards_ar, team_ar,
        clientQuote_ar, clientName_ar
      ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
    `);

    let addedCount = 0;
    for (const project of missingProjects) {
      try {
        stmt.run(
          project.title,
          project.category,
          project.subcategory,
          project.description,
          project.image,
          project.year,
          project.client,
          project.location,
          project.size,
          project.duration,
          project.challenge,
          project.solution,
          project.features,
          project.materials,
          project.awards,
          project.team,
          JSON.stringify([project.image]), // gallery
          project.clientQuote,
          project.clientName,
          project.status,
          project.title_ar,
          project.category_ar,
          project.subcategory_ar,
          project.description_ar,
          project.client_ar,
          project.location_ar,
          project.size_ar,
          project.duration_ar,
          project.challenge_ar,
          project.solution_ar,
          project.features_ar,
          project.materials_ar,
          project.awards_ar,
          project.team_ar,
          project.clientQuote_ar,
          project.clientName_ar
        );
        addedCount++;
        console.log(`✓ Added: ${project.title}`);
      } catch (err) {
        console.log(`⚠ Skipped: ${project.title} (${err.message})`);
      }
    }

    console.log(`\n✓ Successfully added ${addedCount} projects!`);
  } catch (error) {
    console.error('Error adding projects:', error);
    throw error;
  }
}

addMissingProjects().then(() => {
  console.log('Done!');
  process.exit(0);
}).catch(err => {
  console.error('Failed:', err);
  process.exit(1);
});
